unsigned char bit_reverse(unsigned char d) {
	static unsigned char swapped[] = {
        0,  8, 4, 12,
        2, 10, 6, 14,
        1,  9, 5, 13,
        3, 11, 7, 15
    };
	return (((swapped[(d & 0x0f)]) << 4)
	    + swapped[((d & 0xf0) >> 4)]);
}

